func main(){
    let output = reverse(from: n)
    print("The final result is: \(output).")
}    

func reverse(from target: Int) -> Int {
    print("Calculating fibonacci(\(target))...")
    if target <= 2 {
        print("Terminal case, returning 1.")
        return 1
    } else {
        let fibonacci = reverse(from: target - 1) + reverse(from: target - 2)
        print("Non-terminal case, returning \(fibonacci).")
        return fibonacci
    }
}
